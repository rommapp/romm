branch_labels = None
depends_on = None


TABLE = "virtual_collection_roms"
NAME_MAX_LENGTH = 400

# (collection type, generated column holding its values)
COLLECTION_TYPES = [
    ("genre", "generated_genres"),
    ("franchise", "generated_franchises"),
    ("collection", "generated_collections"),
    ("mode", "generated_game_modes"),
    ("company", "generated_companies"),
]

_COLUMNS = "rom_id, type, name, path_cover_s, path_cover_l, created_at, updated_at"


# ---------------------------------------------------------------------------
# Row-producing SELECTs, shared by the backfill and the triggers
# ---------------------------------------------------------------------------


def _mysql_rows(row: str, from_roms: bool) -> str:
    """Membership rows for one rom (``NEW.``) or for every rom (``r.``)."""
    branches = []
    for type_, column in COLLECTION_TYPES:
        json_table = (
            f"JSON_TABLE({row}.{column}, '$[*]' COLUMNS (value TEXT PATH '$')) j"
        )
        source = f"roms r CROSS JOIN {json_table}" if from_roms else json_table
        branches.append(
            f"SELECT DISTINCT {row}.id, '{type_}', LEFT(j.value, {NAME_MAX_LENGTH}), "  # nosec B608
            f"{row}.path_cover_s, {row}.path_cover_l, NOW(), NOW()\n"
            f"FROM {source}\n"
            f"WHERE j.value IS NOT NULL AND j.value != ''"
        )
    return "\nUNION ALL\n".join(branches)


def _postgres_rows(row: str, from_roms: bool) -> str:
    branches = []
    for type_, column in COLLECTION_TYPES:
        # A provider could store a non-array under the key; unnesting that would
        # raise and abort the rom write, so anything else is read as empty.
        array = (
            f"CASE WHEN jsonb_typeof({row}.{column}) = 'array' "
            f"THEN {row}.{column} ELSE '[]'::jsonb END"
        )
        elements = f"jsonb_array_elements_text({array}) AS j(value)"
        source = f"roms r CROSS JOIN LATERAL {elements}" if from_roms else elements
        branches.append(
            f"SELECT DISTINCT {row}.id, '{type_}', LEFT(j.value, {NAME_MAX_LENGTH}), "  # nosec B608
            f"{row}.path_cover_s, {row}.path_cover_l, NOW(), NOW()\n"
            f"FROM {source}\n"
            f"WHERE j.value IS NOT NULL AND j.value != ''"
        )
    return "\nUNION ALL\n".join(branches)


# ---------------------------------------------------------------------------
# Triggers
# ---------------------------------------------------------------------------

_TRACKED_COLUMNS = [column for _, column in COLLECTION_TYPES] + [
    "path_cover_s",
    "path_cover_l",
]


def _mysql_triggers() -> list[str]:
    insert = f"INSERT IGNORE INTO {TABLE} ({_COLUMNS})\n{_mysql_rows('NEW', False)}"
    unchanged = " AND ".join(f"NEW.{c} <=> OLD.{c}" for c in _TRACKED_COLUMNS)
    return [
        f"CREATE TRIGGER virtual_collection_roms_ai AFTER INSERT ON roms\n"  # nosec B608
        f"FOR EACH ROW\n{insert}",
        f"CREATE TRIGGER virtual_collection_roms_au AFTER UPDATE ON roms\n"  # nosec B608
        f"FOR EACH ROW\nBEGIN\n"
        f"IF NOT ({unchanged}) THEN\n"
        f"DELETE FROM {TABLE} WHERE rom_id = NEW.id;\n"
        f"{insert};\n"
        f"END IF;\n"
        f"END",
    ]


def _postgres_trigger_function() -> str:
    unchanged = " AND ".join(
        f"NEW.{c} IS NOT DISTINCT FROM OLD.{c}" for c in _TRACKED_COLUMNS
    )
    return f"""
CREATE OR REPLACE FUNCTION romm_sync_virtual_collection_roms() RETURNS trigger
    LANGUAGE plpgsql AS $$
BEGIN
    IF TG_OP = 'UPDATE' THEN
        IF {unchanged} THEN
            RETURN NULL;
        END IF;
        DELETE FROM {TABLE} WHERE rom_id = NEW.id;
    END IF;

    INSERT INTO {TABLE} ({_COLUMNS})
    {_postgres_rows("NEW", False)}
    ON CONFLICT DO NOTHING;

    RETURN NULL;
END $$
"""  # nosec B608



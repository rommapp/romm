"""Materialize virtual collection membership into an indexed table

``virtual_collections`` was a view that unnested five JSON arrays per rom on
every read, so both the collection list and opening a single collection scanned
the whole library (~40s on 90k roms, regardless of how few roms the collection
held: the name is computed inside the view, so nothing can be narrowed down
early).

Membership now lives in a real table, ``virtual_collection_roms``, holding one
narrow row per (rom, type, name). It is filled from the ``generated_*`` columns
added in 0098 and kept in sync by triggers on ``roms`` (no application hook, no
background job), with rom deletions handled by the foreign key's cascade. The
view is redefined as an aggregate over that table, so a single collection is an
indexed lookup and the list never touches raw provider metadata again.

Notes:
- The backfill pays the old derive-once cost a single time, during this
  migration, instead of on every request.
- Covers are denormalized onto the membership rows, so the list does not join
  back to the (large) roms table; the handler caps how many it reads per
  collection.
- The description now reads "Autogenerated <name> collection" on PostgreSQL
  too; it previously interpolated the type there and the name on MariaDB.
- Names are truncated to the column width instead of relying on the JSON_TABLE
  column type, which would abort the rom write under strict mode.

Revision ID: 0101_virtual_collection_roms
Revises: 0100_roms_facets_table
Create Date: 2026-07-22 00:00:00.000000

"""

import sqlalchemy as sa
